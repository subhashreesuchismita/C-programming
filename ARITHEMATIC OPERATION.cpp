#include<stdio.h>
int main(){
	int n1,n2;
	printf("Enter first number :");
	scanf("%d",&n1);
	printf("Enter second number :");
	scanf("%d",&n2);
	
	printf("ADDITION NUMBER %d\n",n1+n2);
	printf("SUBSTRACTION %d\n",n1-n2);
	printf("MULTIPLICATION %d\n",n1*n2);
	printf("DIVISION %d\n",n1/n2);
}
